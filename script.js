const questions = document.querySelectorAll('.question-box');
const answers = document.querySelectorAll('.answer-box');

const icons = document.querySelectorAll('.icon');
questions.forEach(question => {
    answers.forEach(answer => answer.classList.remove('active'));
    question.addEventListener('click', function () {
        // console.log(this);
        const targetEl = this.closest('.faqs-text-box').querySelector('.answer-box');
        // console.log(targetEl);
        // answers.forEach(answer => answer.classList.remove('active'));
        targetEl.classList.toggle('active');

        icons.forEach(icon => {
            icon.classList.toggle('active');
        })
    })
})


// for splurge image
function changeImage() {
    if (document.getElementById("imgClickAndChange").src == "media/Splurge/on.png"){
        document.getElementById("imgClickAndChange").src = "media/Splurge/off.png";
    } else {
        document.getElementById("imgClickAndChange").src = "media/Splurge/on.png";
    }
}

